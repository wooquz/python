# подключение модуля, переименовали его в m для удобства
import math_operations as m  

def main():
    # Запрос двух числел и преобразование их в float
    x = float(input("Введите первое число: "))
    y = float(input("Введите второе число: "))

    # Вызывов функции из модуля и сразу вывод результата
    print(f"{x} + {y} = {m.add(x, y)}")         # сумма
    print(f"{x} - {y} = {m.subtract(x, y)}")    # разность
    print(f"{x} * {y} = {m.multiply(x, y)}")    # произведение

    # Для деления проверка, не вернула ли функция None
    div = m.divide(x, y)
    if div is None:
        # Если делитель был 0, функция вернула None — сообщение об ошибке
        print("Деление на ноль невозможно.")
    else:
        # Иначе вывод результата деления
        print(f"{x} / {y} = {div}")

if __name__ == "__main__":
    main()